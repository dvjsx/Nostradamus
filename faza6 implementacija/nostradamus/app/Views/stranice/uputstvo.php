            <h2><u>Predvidjanja i ideje</u></h2>
            Nakon što se registrujete na sajt možete da ostavite svoja predviđanja- šta mislite da će se desiti i kada.
            Predviđanja možete da volite, ne volite ili date mišljenje o težini (možete i za sopstveno predviđanje).
            Nakon što istekne datum evaluacije (preporučuje se da uvek stavite dan posle događaja), postavljate 
            status svog predviđanja- da li je ispunjeno ili ne. Ukoliko Vam je predviđanje ispunjeno povećaće Vam se skor u
            zavisnosti od težine predviđanja (ocenjena prema aritmetičkoj sredini korisničkog mišljenja o težini 
            i vremenske distance između davanja predviđanja i ispunjenja).
            <br>
            Nakon što na sajtu provedete 3 dana dobijate mogućnost da postavljate ideje. Ideje su predlog drugim korisnicima
            šta da predviđaju. Možete da odgovorite na neku ideju (bez obzira koliko ste dugo na sajtu) tako što date predviđanje 
            sa naslovom u formatu #NaslovIdeje. Popularnost ideje se meri brojem odgovora na nju, a popularnost korisnika 
            popularnošću njegovih ideja.                      
        </div>
        <div>
            <h2><u>Etika</u></h2>
            Vaša predviđanja i ideje ne smeju sadržati psovke, uvrede, privatne informacije o drugim korisnicima 
            niti se koristiti za izrugivanje određenih osoba ili osetljivih društvenih grupa. Predviđanja i ideje moraju biti smisleni 
            Od Vas se olekuje da budete iskreni u statusu svojih predviđanja. Moderatori će prepravljati statuse vaših predviđanja ukoliko ste slagali,
             administratori će brisati uvredljiva predviđanja i ideje, a i jedni i drugi Vam mogu smanjiti skor ukoliko se ne pridržavate 
             pravila sajta. 
